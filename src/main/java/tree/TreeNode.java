package tree;

/**
 * @Description Definition for a binary main.java.tree node.
 * @Author CHENPENG
 * @Date 2021/8/25
 */
public class TreeNode {
    public int val;
    public TreeNode left;
    public TreeNode right;
    public TreeNode() {}
    public TreeNode(int val) { this.val = val; }
    public TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
